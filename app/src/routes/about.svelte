  <h1>About</h1>

  <p>
    Data Explorer is a website that allows iNaturalist project organizers to import their
    iNaturalist data, upload education materials, and add pre-built interactive data exploration
    tools for their project. By giving citizen scientists the chance to explore the data that they
    help collect, we hope citizen scientists will be encouraged to form their own questions, look
    for answers to their own questions, and thereby create a more engaging experience for both
    participants and organizers.
  </p>
